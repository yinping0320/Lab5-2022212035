
import { Entity, NgField, NgObject, EntityList, NgList, NgDynamic, DynamicEntity, NgEntity } from '@farris/devkit';

@NgEntity({
    originalCode: "Course",
    nodeCode: "courses"
})
export class CourseEntity extends Entity {

    @NgField({
        originalDataField: 'ID',
        dataField: 'id',
        primary: true,
        originalDataFieldType: 'String',
        initValue: '',
        path: 'ID',

        validRules: [
            {
                type: 'required',
                constraints: [true],
            },
            {
                type: 'maxLength',
                constraints: [36],
                message: '最大长度为36',
            }
        ]
    })
    id: string;

    @NgField({
        originalDataField: 'Version',
        dataField: 'version',
        originalDataFieldType: 'DateTime',
        initValue: '0001-01-01T00:00:00',
        path: 'Version',
        enableTimeZone: true,
    })
    version: string;

    @NgField({
        originalDataField: 'CourseId',
        dataField: 'courseId',
        originalDataFieldType: 'String',
        initValue: '',
        path: 'CourseId',

        validRules: [
            {
                type: 'maxLength',
                constraints: [36],
                message: '最大长度为36',
            }
        ]
    })
    courseId: string;

    @NgField({
        originalDataField: 'CourseName',
        dataField: 'courseName',
        originalDataFieldType: 'String',
        initValue: '',
        path: 'CourseName',

        validRules: [
            {
                type: 'maxLength',
                constraints: [36],
                message: '最大长度为36',
            }
        ]
    })
    courseName: string;

    @NgField({
        originalDataField: 'Capacity',
        dataField: 'capacity',
        originalDataFieldType: 'String',
        initValue: '',
        path: 'Capacity',

        validRules: [
            {
                type: 'maxLength',
                constraints: [36],
                message: '最大长度为36',
            }
        ]
    })
    capacity: string;

}